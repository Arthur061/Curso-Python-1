algo = input('\033[34mDigite algo: ')
print('O tipo primitivo desse valor é \033[31m', type (algo))
print('\033[34mSó tem espaços? \033[31m', algo.isspace())
print('\033[34mÉ um número ? \033[31m', algo.isalnum())
print('\033[34mÉ alfabético? \033[31m', algo.isalpha())
print('\033[34mÉ alfanumerico? \033[31m', algo.isalnum())
print('\033[34mEstá em maiusculo? \033[31m', algo.isupper())
print('\033[34mEstá em minusculo? \033[31m', algo. islower())
print('\033[34mEstá capitalizado? \033[31m', algo.istitle())
